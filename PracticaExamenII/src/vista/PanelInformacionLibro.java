    public void generarAno()
    {
        this.jcbAno.removeAllItems();
        int contador = 1980;
        for (int i = 0; i < 36; i++)
        {
            this.jcbAno.addItem(contador);
            contador++;
        }
    }
    
    public String devolverCodigo()
    {
       return this.txtCodigo.getText();
    }
    
    public String [] devolverInformacion()
    {
        String info[]= new String[3];
        info[0]=this.txtCodigo.getText();
        info[1]=this.txtNombre.getText();
        info[2]=""+this.jcbAno.getSelectedIndex();
        
        return info;
    }
    
    public void mostrarInformacion(String arreglo[])
    {
        this.txtNombre.setText(arreglo[1]);
        this.jcbAno.setSelectedIndex(Integer.parseInt(arreglo[2]));
    }
